       
       
       //for loop - will allow you to run certain piece of of code multiple times
        for(int i = 1; i < 20; i=i+2)
        {
			System.out.println("I value is "+i);
        }
        
        //while loop - it will only execute if condition if true otherwise will not run
        int count = 84;
        while(count < 100)
        {
        	System.out.println("values are " +count);
        	count++;
        }
        
        //Do while - same as while loop but will execute at least once
        int ticket = 75;
        do{
        	System.out.println("Current ticket number is " +ticket);
        	ticket++;
        }while(ticket<=100);
        	
